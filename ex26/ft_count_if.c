int		ft_count_if(char **tab, int (*f)(char*))
{
	int		sum;
	int		i;

	sum = 0;
	i = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) == 1)
		{
			sum++;
		}
		i++;
	}
	return (sum);
}
